	<h2>About Social Footprint</h2>
    <p>Social Footprint is a free plugin from <a href="http://www.matthewbarby.com/?utm_source=Social%20Footprint%20Plugin&utm_medium=Plugin&utm_campaign=WordPress%20Plugin
" target="_blank">Matthew Barby</a> designed to give content publishers a better understanding of the social reach of their content.</p>
    <p>To use the plugin, simply navigate to the "<?php echo $this->get_setting('menu-main-title'); ?>" option in the menu on the left. From here you can choose from a few options:</p>
    <ul style="list-style:disc;list-style-position:inside;">
    <li>import all of the posts from your WordPress site to see the number of times they've been shared across social media sites.</li>
    <li>add an individual URL from either your own website or an external website.</li>
    <li>upload a .csv file with a list of URLs that you'd like social share stats for.</li>
</ul>
	<p>One of the advantages to uploading URLs that aren't just from your own domain is that you can track the success of content you've published on other websites. For example, within guest posts. You can then collate all of these stats and get your total 'content social footprint' within the box at the botton of the "<?php echo $this->get_setting('menu-main-title'); ?>" page.</p>
    <p><strong>Note:</strong> social share stats will not automatically update within this section so you will need to click the "Recheck" button to get up-to-date figures.</p>
    <p>If you'd like to only show social share stats from specific social networks, you can update the settings within the "Settings" page. You can select from the following networks:</p>
    <ul style="list-style:disc;list-style-position:inside;">
		<li>Facebook</li>
		<li>Twitter</li>
		<li>Google+</li>
        <li>LinkedIn</li>
		<li>Pinterest</li>
		<li>StumbleUpon</li>
        <li>Reddit</li>
	</ul>
